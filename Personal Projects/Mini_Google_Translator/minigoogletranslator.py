import pandas as pd


# Containers for parsed data
english_sentences = []
telugu_sentences = []

with open("english_telugu_data.txt", "r", encoding="utf-8") as f:
    for line in f:
        if "++++$++++" not in line:
            continue  # Skip malformed lines
        english, telugu = line.strip().split("++++$++++")
        english_sentences.append(english.strip())
        telugu_sentences.append(telugu.strip())

# Step 2: Create a DataFrame
df = pd.DataFrame({"English": english_sentences, "Telugu": telugu_sentences})

# Step 3: Clean any leftover formatting like '++++$++++'
df["English"] = df["English"].str.replace("++++$++++", "", regex=False).str.strip()
df["Telugu"] = df["Telugu"].str.replace("++++$++++", "", regex=False).str.strip()

# Optional: drop too-short rows or blanks
df = df[df["English"].str.len() > 1]
df = df[df["Telugu"].str.len() > 1]

# Step 4: Check result
print(f"✅ Cleaned dataset: {len(df)} sentence pairs")
print(df.sample(5))

# Step 5: Save to clean CSV file
df.to_csv("cleaned_en_te_final.csv", index=False)

