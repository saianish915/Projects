def preprocess_function(examples):
    # Extract lists of English and Telugu sentences
    inputs = [ex["en"] for ex in examples["translation"]]
    targets = [ex["te"] for ex in examples["translation"]]

    # Tokenize inputs and targets
    model_inputs = tokenizer(
        inputs, max_length=128, truncation=True, padding="max_length"
    )

    with tokenizer.as_target_tokenizer():
        labels = tokenizer(
            targets, max_length=128, truncation=True, padding="max_length"
        )

    model_inputs["labels"] = labels["input_ids"]
    return model_inputs
tokenized_ds = hf_dataset.map(preprocess_function, batched=True)

from transformers import Seq2SeqTrainer, Seq2SeqTrainingArguments, DataCollatorForSeq2Seq
import os

# Disable Weights & Biases logging (optional)
os.environ["WANDB_DISABLED"] = "true"

training_args = Seq2SeqTrainingArguments(
    output_dir="./results-en-te",
    do_train=True,
    do_eval=True,
    learning_rate=5e-5,
    per_device_train_batch_size=8,
    per_device_eval_batch_size=8,
    weight_decay=0.01,
    save_total_limit=2,
    num_train_epochs=3,
    predict_with_generate=True,
    logging_dir="./logs",
    logging_steps=50,
    save_steps=500,
    eval_steps=500,
    logging_first_step=True,
    fp16=False
)

data_collator = DataCollatorForSeq2Seq(tokenizer, model=model)

trainer = Seq2SeqTrainer(
    model=model,
    args=training_args,
    train_dataset=tokenized_ds["train"],
    eval_dataset=tokenized_ds["test"],
    tokenizer=tokenizer,
    data_collator=data_collator
)

# STEP 5: Train the Model
trainer.train()

