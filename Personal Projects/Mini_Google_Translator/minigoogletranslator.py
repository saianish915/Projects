from datasets import Dataset

# Load your cleaned dataset
df = pd.read_csv("cleaned_en_te_final.csv")

# Rename columns to match expected format
df = df.rename(columns={"English": "translation_en", "Telugu": "translation_te"})

# Hugging Face wants 'translation': {'en': ..., 'te': ...}
df["translation"] = df.apply(lambda x: {"en": x["translation_en"], "te": x["translation_te"]}, axis=1)

# Convert to HF Dataset
hf_dataset = Dataset.from_pandas(df[["translation"]])
hf_dataset = hf_dataset.train_test_split(test_size=0.1)

from transformers import MarianTokenizer, MarianMTModel

model_name = "Helsinki-NLP/opus-mt-en-dra"
tokenizer = MarianTokenizer.from_pretrained(model_name)
model = MarianMTModel.from_pretrained(model_name)

# Prepend the language token for Telugu
src_text = ">>te<< This is a book."
inputs = tokenizer(src_text, return_tensors="pt", padding=True, truncation=True)
outputs = model.generate(**inputs)
translated = tokenizer.decode(outputs[0], skip_special_tokens=True)
